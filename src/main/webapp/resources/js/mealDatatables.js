var ajaxUrl = "ajax/meals/";
var datatableApi;

function updateTable() {
    $.ajax({
        type: "POST",
        url: ajaxUrl + 'filter',
        data: $('#filter').serialize(),
        success: function (data) {
            datatableApi.clear().rows.add(data).draw();
        }
    });
    return false;
}

function resetFilter() {
    $("#filter").find(":input").val("");
    updateTable();
}

